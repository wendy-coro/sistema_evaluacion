    public static ArrayList<Programa> programa_buscartodos() throws Exception
    {
        //CREO LISTA QUE RECIBIRA LOS DATOS DEL RESULSET
       ArrayList<Programa> lista= new ArrayList<Programa>();
       Programa obj= new Programa();
       ResultSet rs= null;
      //LLAMO LA CONEXION
      Conexion con= new Conexion(Global.driver, Global.url, Global.user, Global.pass);
      //DECLARO UN PREPAREDSTATEMENT QUE EJECUTARA LA SQL
      PreparedStatement preStm= null;

      try {
          //declaro mi sql
          String sql= "select * from public.programa_buscartodos()";
          //creo mi preparedstatement
          preStm=con.creaPreparedSmt(sql);
          //ejecuto el prepardestatement y le asigno a mi resulset
          
          rs= con.ejecutaPrepared(preStm);
          obj=null;
          while (rs.next()) {
              obj= new Programa();
              obj.setProgramaid(rs.getInt("pprogramaid"));
              obj.setNombre(rs.getString("pnombre"));
              obj.setDescripcion(rs.getString("pdescripcion"));
              
              lista.add(obj);
          }
      } catch (SQLException e) {
          System.out.println(e.getMessage());
      }
      finally
      {
          rs.close();
          preStm.close();
          con.desconectar();
      }
            return lista;

    }
    
